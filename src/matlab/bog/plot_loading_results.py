#!/usr/bin/env python
"""
plot_loading_results.py
-----------------------
Plots LH2 loading simulation results from output_loading.mat.
Uses PLOT_SETTINGS.py for thesis-consistent styling.

Place this script in the same directory as:
  - output_loading.mat   (generated by runLoadingSimulation.m)
  - PLOT_SETTINGS.py     (thesis plot style)

Produces:
  figure_transfer_pressure_temp.png  -- (a) Pressure, (b) Temperature
  figure_transfer_mass_balance.png   -- Trailer mass + cumulative BOG
"""

import numpy as np
import scipy.io
import matplotlib
import matplotlib.pyplot as plt
import scienceplots
from matplotlib.ticker import AutoMinorLocator

from PLOT_SETTINGS import (
    colors, graphic_font, math_font, spine_width, linewidth,
    tick_width, tick_length, minor_tick_width, minor_tick_length,
    tick_labelsize, legend_fontsize, label_fontsize, resolution_value,
    style_legend, save_figure,
)


# ── Color aliases ────────────────────────────────────────────────────────────
c_red    = colors[0]   # '#e41a1c'
c_green  = colors[1]   # '#008000'
c_blue   = colors[2]   # '#377eb8'
c_purple = colors[4]   # '#984ea3'
c_grey   = '#666666'


# ── Load data ────────────────────────────────────────────────────────────────
raw = scipy.io.loadmat('output_loading.mat')
out = raw['out'][0, 0]

t_min       = out['t_min'].flatten()
pv1_bar     = out['pv1_bar'].flatten()
pv2_bar     = out['pv2_bar'].flatten()
TL1         = out['TL1'].flatten()
Tv1         = out['Tv1'].flatten()
TL2         = out['TL2'].flatten()
Tv2         = out['Tv2'].flatten()
Tw2         = out['Tw2'].flatten()
mL2         = out['mL2'].flatten()
mv2         = out['mv2'].flatten()
Boiloff_ET  = out['Boiloff_ET'].flatten()
fillpct     = out['fillpct'].flatten()

p_ET_high   = float(out['p_ET_high_bar'].flatten()[0])
p_ET_low    = float(out['p_ET_low_bar'].flatten()[0])


# ── Determine loading stop time ─────────────────────────────────────────────
idx_fill_complete = np.argmax(fillpct >= 90.0)
t_fill_stop = t_min[idx_fill_complete]
print(f'Fill reaches 90% at t = {t_fill_stop:.1f} min')


# ── Axis limits ──────────────────────────────────────────────────────────────
t_max   = 65.0
t_ticks = np.arange(0, 70, 10)


# ── Helpers ──────────────────────────────────────────────────────────────────
def style_ax(ax):
    """Apply PLOT_SETTINGS tick/spine style to an axis."""
    ax.set_axisbelow(False)
    for spine in ax.spines.values():
        spine.set_linewidth(spine_width)
    ax.tick_params(axis='both', which='major', direction='in',
                   width=tick_width, length=tick_length,
                   labelsize=tick_labelsize,
                   bottom=True, top=True, left=True, right=True)
    ax.tick_params(axis='both', which='minor', direction='in',
                   width=minor_tick_width, length=minor_tick_length,
                   bottom=True, top=True, left=True, right=True)
    ax.xaxis.set_minor_locator(AutoMinorLocator(2))
    ax.yaxis.set_minor_locator(AutoMinorLocator(2))


def style_twinx(ax_right, color=None):
    """Style a twinx right-axis without duplicating ticks from the left axis."""
    for spine in ax_right.spines.values():
        spine.set_linewidth(spine_width)
    ax_right.tick_params(axis='y', which='major', direction='in',
                         width=tick_width, length=tick_length,
                         labelsize=tick_labelsize,
                         left=False, right=True)
    ax_right.tick_params(axis='y', which='minor', direction='in',
                         width=minor_tick_width, length=minor_tick_length,
                         left=False, right=True)
    ax_right.tick_params(axis='x', which='both',
                         bottom=False, top=False, labelbottom=False)
    ax_right.yaxis.set_minor_locator(AutoMinorLocator(2))
    if color is not None:
        ax_right.yaxis.label.set_color(color)
        ax_right.tick_params(axis='y', colors=color)
        ax_right.spines['right'].set_color(color)


def add_vline(ax, t):
    """Add a vertical dotted line marking loading completion."""
    ax.axvline(t, color='k', linestyle=':', linewidth=0.9, zorder=5)


def add_panel_label(ax, label):
    """Add bold panel label at the thesis-standard position."""
    ax.text(-0.12, 1.02, rf'\textbf{{({label})}}',
            transform=ax.transAxes, fontsize=label_fontsize,
            va='bottom', ha='right')


# ══════════════════════════════════════════════════════════════════════════════
#  FIGURE 1: Pressure and Temperature (2 panels)
# ══════════════════════════════════════════════════════════════════════════════
with plt.style.context(['ieee']):
    plt.rcParams['font.family']      = graphic_font
    plt.rcParams['mathtext.fontset'] = math_font
    plt.rcParams['text.usetex']      = True

    fig1, (ax1a, ax1b) = plt.subplots(1, 2, figsize=(8, 3))

    # ── (a) Pressure vs time ────────────────────────────────────────────────
    ax1a.plot(t_min, pv1_bar, '-',  color=c_red,  linewidth=linewidth,
              label=r'$P_\mathrm{v}$ (ST)')
    ax1a.plot(t_min, pv2_bar, '-',  color=c_blue, linewidth=linewidth,
              label=r'$P_\mathrm{v}$ (RT)')
    ax1a.axhline(p_ET_high, color=c_grey, linestyle='--', linewidth=0.75,
                 label='RT vent (open)')
    ax1a.axhline(p_ET_low,  color=c_grey, linestyle=':',  linewidth=0.75,
                 label='RT vent (close)')
    add_vline(ax1a, t_fill_stop)

    ax1a.set_xlim(0, t_max)
    ax1a.set_xticks(t_ticks)
    ax1a.set_xlabel(r'Time / [min]', fontsize=label_fontsize)
    ax1a.set_ylabel(r'$P$ / [bar]',  fontsize=label_fontsize)
    style_ax(ax1a)
    style_legend(ax1a, loc='center right', fontsize=legend_fontsize,
                 bbox_to_anchor=(0.96, 0.65), handlelength=1.2, frame=False)
    add_panel_label(ax1a, 'a')

    # ── (b) Temperature vs time ─────────────────────────────────────────────
    # Plot order determines 2-column legend layout.
    # matplotlib ncol=2 fills columns top-to-bottom, then next column:
    #   Col 1 (items 0-2): T_L(ST), T_V(ST), T_wall(RT)
    #   Col 2 (items 3-4): T_L(RT), T_V(RT)
    ax1b.plot(t_min, TL1, '--', color=c_red,    linewidth=linewidth,
              label=r'$T_\mathrm{L}$ (ST)')
    ax1b.plot(t_min, Tv1, '-',  color=c_red,    linewidth=linewidth,
              label=r'$T_\mathrm{V}$ (ST)')
    ax1b.plot(t_min, Tw2, ':',  color=c_purple, linewidth=linewidth,
              label=r'$T_\mathrm{wall}$ (RT)')
    ax1b.plot(t_min, TL2, '--', color=c_blue,   linewidth=linewidth,
              label=r'$T_\mathrm{L}$ (RT)')
    ax1b.plot(t_min, Tv2, '-',  color=c_blue,   linewidth=linewidth,
              label=r'$T_\mathrm{V}$ (RT)')
    add_vline(ax1b, t_fill_stop)

    ax1b.set_xlim(0, t_max)
    ax1b.set_xticks(t_ticks)
    ax1b.set_xlabel(r'Time / [min]', fontsize=label_fontsize)
    ax1b.set_ylabel(r'$T$ / [K]',   fontsize=label_fontsize)
    style_ax(ax1b)
    style_legend(ax1b, loc='center', fontsize=legend_fontsize,
                 ncol=2, bbox_to_anchor=(0.75, 0.70), handlelength=1.2,
                 frame=False, columnspacing=0.8)
    add_panel_label(ax1b, 'b')

    fig1.tight_layout()
    fig1.subplots_adjust(wspace=0.35, right=0.97)
    save_figure(fig1, 'figure_transfer_pressure_temp.png')
    print('Saved figure_transfer_pressure_temp.png')
    plt.close(fig1)


# ══════════════════════════════════════════════════════════════════════════════
#  FIGURE 2: Trailer Mass Balance (single panel, dual y-axis)
# ══════════════════════════════════════════════════════════════════════════════
with plt.style.context(['ieee']):
    plt.rcParams['font.family']      = graphic_font
    plt.rcParams['mathtext.fontset'] = math_font
    plt.rcParams['text.usetex']      = True

    fig2, ax2L = plt.subplots(figsize=(4, 3))
    ax2R = ax2L.twinx()

    m_total = mL2 + mv2

    # Left axis: masses
    ln1 = ax2L.plot(t_min, m_total, '-', color=c_blue,  linewidth=linewidth,
                    label='Total mass')
    ln2 = ax2L.plot(t_min, mL2,     '-', color=c_green, linewidth=linewidth,
                    label='Liquid only')

    # Right axis: cumulative BOG
    ln3 = ax2R.plot(t_min, Boiloff_ET, '-', color=c_red, linewidth=linewidth,
                    label='BOG vented')

    add_vline(ax2L, t_fill_stop)

    ax2L.set_xlim(0, t_max)
    ax2L.set_xticks(t_ticks)
    ax2L.set_xlabel(r'Time / [min]',           fontsize=label_fontsize)
    ax2L.set_ylabel(r'Mass in trailer / [kg]', fontsize=label_fontsize)
    ax2R.set_ylabel(r'Cumulative BOG / [kg]',  fontsize=label_fontsize)

    # Style both axes
    style_ax(ax2L)
    # Remove right-side ticks from the left (black) axis so they don't
    # overlap with the red ticks from the right axis
    ax2L.tick_params(axis='y', which='both', right=False)
    style_twinx(ax2R, color=c_red)

    # Combined legend from both axes (frameless)
    lines  = ln1 + ln2 + ln3
    labels = [l.get_label() for l in lines]
    leg = ax2L.legend(lines, labels, loc='upper left',
                      fontsize=legend_fontsize,
                      handletextpad=0.3, labelspacing=0.5,
                      borderpad=0.5, borderaxespad=0.5,
                      handlelength=1.5, frameon=False)

    fig2.tight_layout()
    save_figure(fig2, 'figure_transfer_mass_balance.png')
    print('Saved figure_transfer_mass_balance.png')
    plt.close(fig2)


# ── Print summary ────────────────────────────────────────────────────────────
m_total_final = mL2[-1] + mv2[-1]
m_total_init  = mL2[0]  + mv2[0]
BOG_total     = Boiloff_ET[-1]
m_delivered   = m_total_final - m_total_init + BOG_total

print(f'\n--- Transfer Loss Summary ---')
print(f'Fill time:        {t_fill_stop:.1f} min')
print(f'Initial mass:     {m_total_init:.1f} kg')
print(f'Final mass:       {m_total_final:.1f} kg')
print(f'BOG vented:       {BOG_total:.1f} kg')
print(f'LH2 delivered:    {m_delivered:.1f} kg')
print(f'Loss fraction:    {BOG_total/m_delivered*100:.2f}%')
print(f'Per 22 trucks/day:')
print(f'  Daily BOG:      {BOG_total*22:.0f} kg/day')
print(f'  Re-liq power:   {BOG_total*22*6.983/24:.1f} kW')
print(f'  % of 25 MW:     {BOG_total*22*6.983/24/25020*100:.2f}%')
print('Done.')